#include<stdio.h>
int main()
{
	float r = 2;
	float pi = 3.14;
	 // tinh chu vi hinh tron
	 float chuvi = 2 * pi * r;
	 printf("Chu vi hinh tron %lf \n", chuvi);
	 
	 // tinh dien tich hinh tron 
	 float dientich = 2 * pi  * r * r;
	 printf("Dien tich hinh tron %lf \n", dientich); 
	 return 0; 
 } 
